public class Variables {
    public static void main(String[] args) {
        // & Varibles in Java : Variables in a programming language refer to the label for an  block of memory where a specific value is stored.
        // ^ Define a variable
        //?  datatype variable_name = value;
        int a = 10;
        int b = 5;

        //? Print value of varibles
        System.out.println(a);
        System.out.println(b);

        //? We can also perform mathematical operations like addition, substraction
        System.out.println(a + b);
        int c = a + b;
        System.out.println(c);
        
    }
}
