public class LogicalOperators {
    public static void main(String[] args) {
        // & Logical Operators in Java : Logical operators are used to combine multiple boolean expressions and return a boolean result based on the logical relationship between them. They are commonly used in conditional statements and loops to control the flow of the program.

        // ? List of Logical Operators in Java:
        // ? 1. Logical AND (&&)
        // ? 2. Logical OR (||)
        // ? 3. Logical NOT (!)

        boolean a = true;
        boolean b = false;

        System.out.println(a && b);   // Output: false
        System.out.println(a || b);   // Output: true
        System.out.println(!a);      // Output: false
        System.out.println(!b);      // Output: true
    }
    
}
