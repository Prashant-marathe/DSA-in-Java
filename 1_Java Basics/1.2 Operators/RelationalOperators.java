public class RelationalOperators {
    public static void main(String[] args) {
        // & Relational Operators in Java : Relational operators are used to compare two values or expressions and return a boolean result (true or false) based on the comparison. They are commonly used in conditional statements and loops to control the flow of the program.

        // ? List of Relational Operators in Java:
        // ? 1. Equal to (==)
        // ? 2. Not equal to (!=)
        // ? 3. Greater than (>)
        // ? 4. Less than (<)
        // ? 5. Greater than or equal to (>=)
        // ? 6. Less than or equal to (<=)

        int a = 10;
        int b = 20;

        System.out.println(a == b);   // Output: false
        System.out.println(a != b);   // Output: true
        System.out.println(a > b);    // Output: false
        System.out.println(a < b);    // Output: true
        System.out.println(a >= b);   // Output: false
        System.out.println(a <= b);   // Output: true
    }
    
}
