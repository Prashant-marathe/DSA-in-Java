public class AssignmentOperators {
    public static void main(String[] args) {
        // & Assignment Operators in Java : Assignment operators are used to assign values to variables. They are commonly used in programming to initialize variables and update their values.

        // ? List of Assignment Operators in Java:
        // ? 1. Simple Assignment (=)
        // ? 2. Add and Assign (+=)
        // ? 3. Subtract and Assign (-=)
        // ? 4. Multiply and Assign (*=)
        // ? 5. Divide and Assign (/=)
        // ? 6. Modulus and Assign (%=)

        int a = 10;      // Simple Assignment
        a += 5;          // Add and Assign (a = a + 5)
        System.out.println(a);   // Output: 15

        a -= 3;          // Subtract and Assign (a = a - 3)
        System.out.println(a);   // Output: 12

        a *= 2;          // Multiply and Assign (a = a * 2)
        System.out.println(a);   // Output: 24

        a /= 4;          // Divide and Assign (a = a / 4)
        System.out.println(a);   // Output: 6

        a %= 5;          // Modulus and Assign (a = a % 5)
        System.out.println(a);   // Output: 1
    }
    
}
